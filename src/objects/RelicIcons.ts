import Phaser from 'phaser';

const PX = 5,
  G = 6,
  OFF = 1;

type Grid = string[];

function render(ctx: CanvasRenderingContext2D, grid: Grid, main: string, alt?: string) {
  ctx.fillStyle = '#0a0a14';
  ctx.fillRect(0, 0, 32, 32);
  for (let r = 0; r < G; r++) {
    for (let c = 0; c < grid[r].length; c++) {
      const ch = grid[r][c];
      if (ch === '.') continue;
      ctx.fillStyle = ch === 'A' && alt ? alt : main;
      ctx.fillRect(c * PX + OFF, r * PX + OFF, PX, PX);
    }
  }
}

