export function generateRelicTextures(scene: Phaser.Scene) {
  DEFS.forEach(([id, grid, main, alt]) => {
    const tex = scene.textures.createCanvas(`relic_${id}`, 32, 32);
    if (!tex) return;
    render(tex.context, grid, main, alt);
    tex.refresh();
  });
}
